    <h1>Biography of Rocky Balboa</h1><br>
    <p class="p">Robert "Rocky" Balboa was born in Philadelphia, Pennsylvania, on July 6, 1945. He was the only child in a Roman Catholic Italian-American family. However, the surname Balboa (Italian pronunciation: [balˈbɔːa]; roughly meaning "beautiful valley") originates from a Galician-speaking town in northwestern Spain. When Rocky is spoken to by his priest, Father Carmine, it is apparent that he understands Italian very well, including a scene in which he translates for Tommy Gunn. It is undetermined how well he actually speaks the language, as his responses are always in English.
